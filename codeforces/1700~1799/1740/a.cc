using namespace std;

int main(void) {
  ios::sync_with_stdio(false);
  cin.tie(0);

  int T = 0;
  cin >> T;
  for (int t = 0; t < T; ++t) {
    int a = 0;
    cin >> a;
    if (a == 2) {
      cout << "2" << endl;
    } else {
      cout << "3" << endl;
    }
  }
  return 0;
}
